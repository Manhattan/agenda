package br.com.grupopibb.portalobra.utils;

/**
 *
 * @author arthur.diego
 */
public class ListaPedidos {
    
    private String itemItem;
    private String idSistema;
    private String insumoCod;
    private String codigo;
    private String especificaoObsersavao;
    private String quantidade;
    private String precoInsumo;
    private String aliqIpi;
    private String valorTotalItemComIPI;

    

    public String getItemItem() {
        return itemItem;
    }

    public void setItemItem(String itemItem) {
        this.itemItem = itemItem;
    }

    public String getIdSistema() {
        return idSistema;
    }

    public void setIdSistema(String idSistema) {
        this.idSistema = idSistema;
    }

    public String getInsumoCod() {
        return insumoCod;
    }

    public void setInsumoCod(String insumoCod) {
        this.insumoCod = insumoCod;
    }

    public String getEspecificaoObsersavao() {
        return especificaoObsersavao;
    }

    public void setEspecificaoObsersavao(String especificaoObsersavao) {
        this.especificaoObsersavao = especificaoObsersavao;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }

    public String getPrecoInsumo() {
        return precoInsumo;
    }

    public void setPrecoInsumo(String precoInsumo) {
        this.precoInsumo = precoInsumo;
    }

    public String getAliqIpi() {
        return aliqIpi;
    }

    public void setAliqIpi(String aliqIpi) {
        this.aliqIpi = aliqIpi;
    }

    public String getValorTotalItemComIPI() {
        return valorTotalItemComIPI;
    }

    public void setValorTotalItemComIPI(String valorTotalItemComIPI) {
        this.valorTotalItemComIPI = valorTotalItemComIPI;
    }
    
    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }
    
}
