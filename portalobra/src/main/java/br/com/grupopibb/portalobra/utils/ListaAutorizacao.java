package br.com.grupopibb.portalobra.utils;

/**
 *
 * @author arthur.diego
 */
public class ListaAutorizacao {
    
    private String codigo;
    private String discriminacao;
    private String unidade;
    private String quantidade;
    private String precoUnitAbrev;
    private String custo;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getDiscriminacao() {
        return discriminacao;
    }

    public void setDiscriminacao(String discriminacao) {
        this.discriminacao = discriminacao;
    }

    public String getUnidade() {
        return unidade;
    }

    public void setUnidade(String unidade) {
        this.unidade = unidade;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }

    public String getPrecoUnitAbrev() {
        return precoUnitAbrev;
    }

    public void setPrecoUnitAbrev(String precoUnitAbrev) {
        this.precoUnitAbrev = precoUnitAbrev;
    }

    public String getCusto() {
        return custo;
    }

    public void setCusto(String custo) {
        this.custo = custo;
    }
    
    
    
}
